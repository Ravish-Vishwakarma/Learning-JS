const e = 2.718

// It compares the value only
// != not equal
if (e == "2.718") {
    console.log("== Its equal")
} else {
    console.log("== Its not equal")
}


// It compares the value and data type both
// !== not equal
if (e === "2.718") {
    console.log("=== Its equal")
} else {
    console.log("=== Its not equal")
}
